const mongoose = require('mongoose');

const TaskSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  status: {
    type: String,
    enum: ['Incomplete', 'Completed'],
    default: 'Incomplete',
  },
});

module.exports = mongoose.model('Task', TaskSchema);
